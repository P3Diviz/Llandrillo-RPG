/* ── PARTICLES ──────────────────────────────────────────────── */

function fireParticles() {
  var t = getTheme();
  for (var i = 0; i < 100; i++) {
    (function (delay) {
      setTimeout(function () {
        createParticle(t.colours[Math.floor(Math.random() * t.colours.length)]);
      }, delay);
    })(i * 28);
  }
}

function createParticle(colour) {
  var p = document.createElement('div');
  var size = Math.random() * 9 + 4;
  var startX = Math.random() * window.innerWidth;
  var vx = (Math.random() - 0.5) * 420;
  var vy = -(Math.random() * 500 + 160);

  p.style.cssText = [
    'position:fixed',
    'width:' + size + 'px',
    'height:' + size + 'px',
    'background:' + colour,
    'border-radius:' + (Math.random() > 0.5 ? '50%' : '3px'),
    'left:' + startX + 'px',
    'top:' + window.innerHeight + 'px',
    'pointer-events:none',
    'z-index:9999',
  ].join(';');

  document.body.appendChild(p);

  var posX = startX;
  var posY = window.innerHeight;
  var opacity = 1;

  var timer = setInterval(function () {
    vy += 14;
    posX += vx / 60;
    posY += vy / 60;
    opacity -= 0.013;
    p.style.left = posX + 'px';
    p.style.top = posY + 'px';
    p.style.opacity = opacity;
    if (opacity <= 0 || posY > window.innerHeight + 20) {
      clearInterval(timer);
      if (p.parentNode) {
        p.parentNode.removeChild(p);
      }
    }
  }, 16);
}

/* ── MESSAGE OVERLAY ────────────────────────────────────────── */

