/* ── SET STAT BARS ON PAGE LOAD ─────────────────────────────── */

window.addEventListener('load', function () {
  setStatBars();
});

function setStatBars() {
  var bars = document.querySelectorAll('.bar-fill');
  for (var i = 0; i < bars.length; i++) {
    var pts = parseInt(bars[i].getAttribute('data-points'), 10) || 0;
    if (pts > 4) {
      pts = 4;
    }
    if (pts < 0) {
      pts = 0;
    }
    bars[i].style.width = (pts / 4) * 100 + '%';
  }
}

/* ── ACTIVATE POWER — called by student's button onclick ─────── */

function activatePower() {
  var btn = document.querySelector('button');
  if (btn) {
    btn.disabled = true;
    btn.textContent = 'Pwerau Actif / Powers Active';
  }
  fireParticles();
  showMessage();
}

